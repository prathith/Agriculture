<div style="font-weight:bold;font-size:25px;color:black;background:lightgreen;width:98.5vw;position:absolute;top:100%;left:0px;padding:10px;">
<h1 style="font-weight:bold;font-size:30px;text-align: center;">What is a GMO?</h1>
<p class="p1">GMO stands for Genetically Modified Organism, and it can describe the way many products in industries like medicine, scientific research and agriculture are made. When it comes to food, GMO refers to seeds. They grow in the ground like any other seed, only genetically modified (GM) seeds have certain desirable traits setting them apart. These seeds grow into plants that might use water more efficiently or better withstand pests like bugs or weeds, which means they may require less farmland to grow.</p>
<p>GM seeds have been used by farmers for approximately 20 years. However, seeds have actually been modified through traditional plant breeding techniques for hundreds of years. Fruits like today's seedless watermelon and bananas, which are significantly different than earlier versions of these fruits, are a result of traditional plant breeding techniques. Modern GM seeds still make use of traditional plant breeding to add desired traits to plants—essentially combining cutting-edge and foundational plant science.</p></span>
<div style="font-weight:bold;font-size:25px;color:black;background:lightblue;width:98.5vw;position:absolute;top:100%;left:0px;padding:10px;">
<p class="p3 c	">How GM seeds are developed:</p>
<span style="font-weight:bold;font-size:25px;color:black;background:lightblue;width:98.5vw;position:absolute;top:60%;left:0px;padding:10px;text-align:left;">
<ol><li>The desired trait (like disease resistance or drought tolerance) is identified in nature.</li>
    <li>The gene for the trait is transferred into the plant seed.</li>
    <li>The plant is tested to ensure that it is safe for people, animals, and the environment.</li>
    <li>After years of testing and approvals from governments around the world, the new seeds<br> are available to farmers.</li></ol></span></div> 

<img src="gmo.jpg" height=300px width=300px style="position:absolute;top:60%;left:60%;">
<p style="width:50%">Why Grow GM Crops?<br><span class="re">If you're wondering why farmers choose to grow genetically modified crops, it helps to consider the challenges they face every day: crop loss from weeds, insects, and disease, and the effects of climate change such as drought, just to name a few. Not only do seeds improved through genetic modification help them combat these challenges, but it can help them to use less of our precious resources like water and land.</span></p>

<div style="font-weight:bold;background:lightblue;font-size:25px;color:black;width:97.5vw;position:absolute;height:300px;padding:10px;"></div>
<p class="" style="position:absolute;left:300px;">Efficient Use of Natural Resources</p>
<p style="position:absolute;left:300px;top:107%;">Some GM crops can use water more efficiently than conventional crops—meaning farmers can ultimately water their fields less often. Some GM seeds also promote the use of no-till farming, which keeps more moisture in the soil. This no-till method also reduces farming’s carbon footprint by using less fuel and keeping more carbon dioxide in the ground.</p></div>
